# 2. –í–∏–≤–µ—Å—Ç–∏ —Ç–µ–∫—Å—Ç, –≤ —è–∫–æ–º—É —Ç–µ–∫—Å—Ç–æ–≤—ñ —Å–º–∞–π–ª–∏–∫–∏ –∑–∞–º—ñ–Ω–µ–Ω—ñ –Ω–∞ –≤—ñ–¥–ø–æ–≤—ñ–¥–Ω—ñ –µ–º–æ–¥–∂—ñ.

# –ö–∞—Ä—Ç–∞ –≤—ñ–¥–ø–æ–≤—ñ–¥–Ω–æ—Å—Ç—ñ —Å–º–∞–π–ª–∏–∫–∞ –¥–æ –µ–º–æ–¥–∂—ñ:

# - `:)` = üôÇ U0001F642
# - `XD` = üòÇ U0001F923
# - `:(` = üôÅ U0001F641
# - `>:(` = üò† U0001F620
# - `>:)` = üòà U0001F608

dict_emoji = {'>:(': '\U0001F620',
              '>:)': '\U0001F608',
              ':)': '\U0001F642',
              'XD': '\U0001F923',
              ':(': '\U0001F641'}

string = input("–í–≤–µ–¥—ñ—Ç—å —Ñ—Ä–∞–∑—É –∑—ñ —Å–º–∞–π–ª–∏–∫–∞–º–∏ \U0001F606 :")

print("–í–∏ –≤–≤–µ–ª–∏ —Ñ—Ä–∞–∑—É:", string)

for key in dict_emoji:

    print(key, "=", dict_emoji[key])

    string = string.replace(key, dict_emoji[key])

print(string)
